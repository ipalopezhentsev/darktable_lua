"""Quality / regression suite for auto_negadoctor.

Iterates over every annotated roll under tests/fixtures/rolls/ that has local
TIFF exports (discover_rolls), running two layers per roll:
  1. Baseline-independent INVARIANTS over a fresh pipeline run (param ranges,
     wb normalization, exposure-ordering consistency of Dmin across frames,
     patches inside frame bounds and outside the holder border, hex round-trip),
     plus the crop-containment / ground-truth / AI-variant / no-clipping gates
     against that roll's committed annotations.
  2. Baseline diff vs tests/baseline_session/<roll_id>/{stem}_debug_nega.json
     (generated by generate_baseline.py after user approval): global winner
     identity, film-base location, params within tolerances.

Run: conda run -n autocrop python auto_negadoctor/tests/run_quality_tests.py
Exit code: 0 = PASS or WARN, 1 = FAIL.
"""

import json
