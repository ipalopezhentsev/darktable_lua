def load_baseline(roll_id):
    """Baselines are per-roll (stems collide across rolls):
    baseline_session/<roll_id>/*_debug_nega.json."""
    d = BASELINE_DIR / roll_id
    if not d.is_dir():
        return {}
    out = {}
    for f in sorted(d.glob("*_debug_nega.json")):
        with open(f) as fh:
            data = json.load(fh)
        out[data["stem"]] = data
    return out


def rect_center(rect):
    return (rect[0] + rect[2] / 2.0, rect[1] + rect[3] / 2.0)


def diff_frame_vs_baseline(fr, base):
    """Return list of difference strings for one frame."""
    diffs = []
    p, bp = fr["params"], base["params"]

    for c in range(3):
        rel = abs(p["Dmin"][c] / max(bp["Dmin"][c], 1e-9) - 1.0)
        if rel > TOL_DMIN_REL:
            diffs.append(f"Dmin[{c}] {p['Dmin'][c]:.4f} vs {bp['Dmin'][c]:.4f}")
        if abs(p["wb_low"][c] - bp["wb_low"][c]) > TOL_WB:
            diffs.append(f"wb_low[{c}] {p['wb_low'][c]:.3f} vs {bp['wb_low'][c]:.3f}")
        if abs(p["wb_high"][c] - bp["wb_high"][c]) > TOL_WB:
            diffs.append(f"wb_high[{c}] {p['wb_high'][c]:.3f} vs {bp['wb_high'][c]:.3f}")
    if abs(p["D_max"] - bp["D_max"]) > TOL_DMAX:
        diffs.append(f"D_max {p['D_max']:.3f} vs {bp['D_max']:.3f}")
    if abs(p["black"] - bp["black"]) > TOL_BLACK:
        diffs.append(f"black {p['black']:.4f} vs {bp['black']:.4f}")
    if abs(p["exposure"] - bp["exposure"]) > TOL_EXPOSURE:
        diffs.append(f"exposure {p['exposure']:.4f} vs {bp['exposure']:.4f}")

    base_local = (base.get("film_base") or {}).get("local")
    ours_local = fr.get("base")
    if bool(base_local) != bool(ours_local):
        diffs.append(f"film-base candidate presence changed "
                     f"({bool(ours_local)} vs {bool(base_local)})")
    elif base_local and ours_local:
        bx, by = rect_center(base_local["rect"])
        ox, oy = rect_center(ours_local["rect"])
        d = math.hypot(ox - bx, oy - by)
        if d > TOL_PATCH_CENTER_PX:
            diffs.append(f"film-base moved {d:.0f}px "
                         f"({ours_local['rect']} vs {base_local['rect']})")

    # highlight patch: presence, position AND size (sizes are user-tunable
    # via the debug UI and must survive algorithm changes)
    base_patches = base.get("patches") or {}
    for kind, key in (("highlights", "highlight_patch"),):
        b_obj = base_patches.get(kind) or {}
        b_rect = b_obj.get("rect")
        o_obj = fr.get(key)
        o_rect = o_obj["rect"] if o_obj else None
        if bool(b_rect) != bool(o_rect):
            diffs.append(f"{kind} patch presence changed "
                         f"({bool(o_rect)} vs {bool(b_rect)})")
            continue
        if not b_rect:
            continue
        bx, by = rect_center(b_rect)
        ox, oy = rect_center(o_rect)
        d = math.hypot(ox - bx, oy - by)
        if d > TOL_PATCH_CENTER_PX:
            diffs.append(f"{kind} patch moved {d:.0f}px ({o_rect} vs {b_rect})")
        if (abs(o_rect[2] - b_rect[2]) > TOL_PATCH_SIZE_PX
                or abs(o_rect[3] - b_rect[3]) > TOL_PATCH_SIZE_PX):
            diffs.append(f"{kind} patch size changed "
                         f"{o_rect[2]}x{o_rect[3]} vs {b_rect[2]}x{b_rect[3]}")
    return diffs


def check_crop_containment(frames, fixtures):
    """HARD RULE (user, 2026-06-12): the detected content crop must NEVER
    extend outside the user's hand-drawn crop annotations — the user rects
    are ground truth and anything outside them is holder/junk that poisons
    the level analysis. Any violation FAILS the suite.

    Returns a list of violation strings."""
    violations = []
    by_stem = {fr["stem"]: fr for fr in frames}
    checked = 0
    for f in fixtures:
        data = json.loads(f.read_text())
        crop = data.get("crop_correction")
        if not crop or not crop.get("corrected"):
            continue
        stem = data.get("stem") or f.name.replace("_annotations.json", "")
        fr = by_stem.get(stem)
        if fr is None or fr.get("error") or "border" not in fr:
            continue
        checked += 1
        x, y, cw, ch = _rect_to_px(crop["corrected"], fr["width"], fr["height"])
        user = (x, y, fr["width"] - x - cw, fr["height"] - y - ch)
        det = fr["border"]
        # The user crops are stored as normalized fractions; denormalizing them
        # onto a frame of a different resolution than they were drawn at rounds
        # by up to 1px. Allow that slack so cross-resolution rounding is not a
        # spurious "crop extends outside" violation (real over-includes are
        # many px - DSC_0037's was 50).
        for i, edge in enumerate(("left", "top", "right", "bottom")):
            if det[i] < user[i] - CROP_CONTAINMENT_ROUND_TOL:
                violations.append(
                    f"{stem}: detected {edge} margin {det[i]} < user "
                    f"{user[i]} (crop extends outside the user rect) "
                    f"[{f.parent.name}]")
    print(f"  containment checked on {checked} crop fixture(s), "
          f"{len(violations)} violation(s)")
    return violations


def crop_overtrim_per_frame(frames, fixtures):
    """Per-frame crop closeness vs the user's hand-drawn crop annotations, as
    FRACTIONS of the frame (resolution-independent — never pixels). The
    algorithm-INDEPENDENT crop metric: it compares the detected crop RECTANGLE
    to the annotated rectangle, never the detector internals.

    For each frame carrying a crop_correction:
      - contained: detected crop stays inside the user rect (the HARD rule;
        CROP_CONTAINMENT_ROUND_TOL px slack for cross-resolution denorm).
      - overtrim_area: fraction of FRAME AREA of content the user kept but the
        detected crop removed (= user_area − intersection(user, detected)). 0
        when the detected crop covers the whole user rect.
      - overtrim_edges: per-edge over-trim as a fraction of that edge's
        dimension (left/right over width, top/bottom over height); a NEGATIVE
        value means the detected crop extends OUTSIDE the user rect on that edge
        (a containment violation).
    Returns a list of {stem, contained, overtrim_area, overtrim_edges}."""
    by_stem = {fr["stem"]: fr for fr in frames}
    records = []
    for f in fixtures:
        data = json.loads(f.read_text())
        crop = data.get("crop_correction")
        if not crop or not crop.get("corrected"):
            continue
        stem = data.get("stem") or f.name.replace("_annotations.json", "")
        fr = by_stem.get(stem)
        if fr is None or fr.get("error") or "border" not in fr:
            continue
        rec = crop_overtrim(fr["border"], crop["corrected"],
                            fr["width"], fr["height"])
        records.append(dict(rec, stem=stem))
    return records


def crop_overtrim(border, crop_corrected, w, h):
    """Pure crop-closeness record for ONE frame (no I/O): the detected `border`
    margins (l, t, r, b in px) vs the user's normalized crop rect
    [x, y, rw, rh]. Returns {contained, overtrim_area, overtrim_edges} — all
    FRACTIONS (see crop_overtrim_per_frame). Reused by the calibration runner on
    cached frames so the gate and the runner share ONE metric."""
    x, y, cw, ch = _rect_to_px(crop_corrected, w, h)
    user = (x, y, w - x - cw, h - y - ch)            # (l, t, r, b) margins, px
    det = border
    dims = (w, h, w, h)
    edges, contained = {}, True
    for i, name in enumerate(("left", "top", "right", "bottom")):
        edges[name] = (det[i] - user[i]) / float(dims[i])   # +ve = over-trim
        if det[i] < user[i] - CROP_CONTAINMENT_ROUND_TOL:
            contained = False
    # content lost = user rect area minus its intersection with the detected
    # kept rectangle (correct whether or not the crop is contained).
    ux0, uy0, ux1, uy1 = x, y, x + cw, y + ch
    dx1, dy1 = w - det[2], h - det[3]
    iw = max(0, min(ux1, dx1) - max(ux0, det[0]))
    ih = max(0, min(uy1, dy1) - max(uy0, det[1]))
    inter = iw * ih
    user_area = max(0, cw) * max(0, ch)
    overtrim_area = max(0.0, user_area - inter) / float(w * h)
    # border LEFT IN = detected kept area minus its intersection with the user
    # rect (the mirror of overtrim_area; >0 exactly when the crop is NOT
    # contained — holder/junk the detector kept outside the user's rect).
    det_area = max(0, dx1 - det[0]) * max(0, dy1 - det[1])
    undertrim_area = max(0.0, det_area - inter) / float(w * h)
    return {"contained": contained, "overtrim_area": overtrim_area,
            "undertrim_area": undertrim_area, "overtrim_edges": edges}


def selftest_crop_overtrim():
    """Test for the metric: an exact-match crop over-trims 0; a known inset
    over-trims the right area fraction; a crop extending outside the user rect
    reads contained=False with a negative edge."""
    base = {"stem": "X", "error": None, "width": 100, "height": 100}
    # user keeps the central 80x80 (margins 10 all round, as fractions)
    crop_ann = {"crop_correction": {"corrected": [0.10, 0.10, 0.80, 0.80]}}

    def rec(border):
        fr = dict(base, border=border)
        fx = _FakeFixture(dict(crop_ann, stem="X"))
        return crop_overtrim_per_frame([fr], [fx])[0]

    exact = rec((10, 10, 10, 10))
    assert exact["contained"] and abs(exact["overtrim_area"]) < 1e-9, exact
    # trim 10px more on the left -> kept 70x80 inside the 80x80 user rect:
    # lost 10*80 = 800 px of a 10000 px frame = 0.08
    inset = rec((20, 10, 10, 10))
    assert inset["contained"] and abs(inset["overtrim_area"] - 0.08) < 1e-9, inset
    assert abs(inset["overtrim_edges"]["left"] - 0.10) < 1e-9, inset
    assert abs(inset["undertrim_area"]) < 1e-9, inset    # contained -> no border
    # detected extends 5px outside the user rect on the left -> not contained;
    # border left in = 5px * 80px tall kept = 400/10000 = 0.04
    out = rec((5, 10, 10, 10))
    assert not out["contained"] and out["overtrim_edges"]["left"] < 0, out
    assert abs(out["undertrim_area"] - 0.04) < 1e-9, out
    print("Crop-overtrim metric self-test: OK")


class _FakeFixture:
    """Minimal stand-in for a fixtures Path with .read_text()/.name/.parent."""
    def __init__(self, data):
        import json as _json
        self._text = _json.dumps(data)
        self.name = f"{data.get('stem', 'X')}_annotations.json"

        class _P:
            name = "selftest"
        self.parent = _P()

    def read_text(self):
        return self._text


def report_annotated_frames(frames, fixtures):
    """Informational check against committed user annotations
    (fixtures/rolls/<roll_id>/.../{stem}_annotations.json): show whether the
    detector now finds patches on frames the user had to correct, and how
    the wb from the user's rects compares to what was applied."""
    import auto_negadoctor as an
    import nega_model as nm
    if not fixtures:
        print("  (no annotation fixtures)")
        return
    by_stem = {fr["stem"]: fr for fr in frames}
    for f in fixtures:
        data = json.loads(f.read_text())
        stem = data.get("stem") or f.name.replace("_annotations.json", "")
        fr = by_stem.get(stem)
        if fr is None or fr.get("error") or "params" not in fr:
            print(f"  {stem} [{f.parent.name}]: not in current run")
            continue
        print(f"  {stem} [{f.parent.name}]:")
        try:
            lin = an.load_frame(fr["path"])[1]
        except Exception as e:
            print(f"    cannot load frame: {e}")
            continue
        p = fr["params"]
        for kind, entry in (data.get("patch_corrections") or {}).items():
            # the shadow patch was removed (wb_low is region-based); ignore any
            # such corrections left in older annotation sessions
            if kind == "shadows":
                continue
            rect = entry.get("corrected")
            if not rect:
                continue
            x, y, w, h = _rect_to_px(rect, fr["width"], fr["height"])
            rgb = [float(v) for v in lin[y:y + h, x:x + w].reshape(-1, 3).mean(axis=0)]
            key = {"highlights": "highlight_patch", "film_base": "base"}.get(kind)
            found = bool(fr.get(key)) if key else False
            line = f"    {kind}: detector {'FOUND a patch' if found else 'fell back'}"
            try:
                if kind == "highlights":
                    wb = nm.compute_wb_high(p["Dmin"], rgb, p["D_max"],
                                            p["offset"], p["wb_low"])
                    line += (f"; user-rect wb_high ({', '.join(f'{v:.3f}' for v in wb)})"
                             f" vs applied ({', '.join(f'{v:.3f}' for v in p['wb_high'])})")
            except (ValueError, ZeroDivisionError, OverflowError):
                line += "; user-rect wb DEGENERATE (patch too close to film base)"
            print(line)
        for kind, entry in (data.get("wb_overrides") or {}).items():
            chosen = entry.get("corrected")
            applied = p.get("wb_low" if kind == "shadows" else "wb_high")
            if chosen and applied:
                print(f"    wb {kind} (wheel-picked): user "
                      f"({', '.join(f'{v:.3f}' for v in chosen)}) vs applied "
                      f"({', '.join(f'{v:.3f}' for v in applied)})  "
                      "<- tuning target")
            else:
                print(f"    wb {kind} (wheel-picked): user {chosen}")
        for name, entry in (data.get("print_overrides") or {}).items():
            applied = p.get(name)
            corrected = entry.get("corrected")
            if applied is not None and corrected is not None:
                print(f"    print {name}: user {corrected:.4f} vs applied "
                      f"{applied:.4f} ({corrected - applied:+.4f})")
            else:
                print(f"    print {name}: user {corrected}")
        crop = data.get("crop_correction")
        if crop and crop.get("corrected"):
            l, t, r, b = fr.get("border") or (0, 0, 0, 0)
            auto_rect = [l, t, fr["width"] - l - r, fr["height"] - t - b]
            user_rect = _rect_to_px(crop["corrected"], fr["width"], fr["height"])
            print(f"    crop: user {user_rect} vs auto border "
                  f"rect {auto_rect} (holder detection tuning signal)")


def _load_ground_truth(fixtures):
    """Per-stem, field-level last-writer-wins map of the user's hand-tuned
    target params, gathered from one roll's annotation sessions. Wheel-picked
    `wb_overrides` -> wb_low/wb_high; `print_overrides` -> black/exposure/gamma.
    Later dated sessions (e.g. 2026-06-13_wb_print_roll) override earlier ones
    (e.g. 2026-06-11_taste) for the same stem+field. Returns {stem: gt}."""
    gt_by_stem = {}
    for f in fixtures:
        data = json.loads(f.read_text())
        stem = data.get("stem") or f.name.replace("_annotations.json", "")
        gt = gt_by_stem.setdefault(stem, {})
        touched = False
        for kind, key in (("shadows", "wb_low"), ("highlights", "wb_high")):
            entry = (data.get("wb_overrides") or {}).get(kind) or {}
            corrected = entry.get("corrected")
            if corrected:
                gt[key] = [float(v) for v in corrected]
                touched = True
        for name in ("D_max", "offset", "black", "exposure", "gamma"):
            entry = (data.get("print_overrides") or {}).get(name) or {}
            corrected = entry.get("corrected")
            if corrected is not None:
                gt[name] = float(corrected)
                touched = True
        if touched:
            gt["session"] = f.parent.name
    return {s: gt for s, gt in gt_by_stem.items() if gt}


def _ground_truth_violations(stem, params, gt):
    """Compare current algorithm params against one stem's ground truth.
    Returns (violations, deltas): a list of strings and a {param: abs_delta}
    dict (per wb component keyed e.g. 'wb_low[1]'). Pure — no I/O."""
    violations, deltas = [], {}
    session = gt.get("session", "?")

    def note(label, applied, target, tol):
        d = abs(applied - target)
        deltas[label] = d
        if d > tol:
            violations.append(
                f"{stem}: {label} target {target:.4f} vs applied "
                f"{applied:.4f} (Δ{d:.4f} > {tol}) [{session}]")

    for key in ("wb_low", "wb_high"):
        target = gt.get(key)
        applied = params.get(key)
        if target and applied:
            for c in range(3):
                note(f"{key}[{c}]", applied[c], target[c], TOL_GT_WB)
    for name, tol in (("D_max", TOL_GT_DMAX),
                      ("offset", TOL_GT_OFFSET),
                      ("black", TOL_GT_BLACK),
                      ("exposure", TOL_GT_EXPOSURE),
                      ("gamma", TOL_GT_GAMMA)):
        target = gt.get(name)
        applied = params.get(name)
        if target is not None and applied is not None:
            note(name, applied, target, tol)
    return violations, deltas


def check_ground_truth(frames, fixtures):
    """HARD gate: the production params must match the user's hand-tuned
    wheel/print annotations (the ground truth this feature must converge to).
    Strict tolerances; expected to FAIL until the algorithm is tuned. Returns
    a list of violation strings and prints a per-param aggregate summary."""
    gt_by_stem = _load_ground_truth(fixtures)
    if not gt_by_stem:
        print("  (no wb/print ground-truth annotations)")
        return []
    by_stem = {fr["stem"]: fr for fr in frames}
    violations = []
    all_deltas = {}   # label -> [abs deltas across frames]
    checked = 0
    for stem, gt in sorted(gt_by_stem.items()):
        fr = by_stem.get(stem)
        if fr is None or fr.get("error") or "params" not in fr:
            continue
        checked += 1
        vios, deltas = _ground_truth_violations(stem, fr["params"], gt)
        violations.extend(vios)
        for label, d in deltas.items():
            all_deltas.setdefault(label, []).append(d)

    # aggregate dashboard: group per-component wb into wb_low/wb_high
    def group(label):
        return label.split("[")[0]
    grouped = {}
    for label, ds in all_deltas.items():
        grouped.setdefault(group(label), []).extend(ds)
    print(f"  checked {checked} frame(s) against ground truth, "
          f"{len(violations)} violation(s)")
    if grouped:
        print("  aggregate abs delta (algorithm vs ground truth):")
        for name in ("wb_low", "wb_high", "black", "exposure", "gamma"):
            ds = grouped.get(name)
            if not ds:
                continue
            ds_sorted = sorted(ds)
            median = ds_sorted[len(ds_sorted) // 2]
            print(f"    {name:9s} median {median:.4f}  max {max(ds):.4f}  "
                  f"(n={len(ds)})")
    return violations


def selftest_ground_truth():
    """Test for the test: clean params pass, off params are flagged per param."""
    gt = {"wb_low": [1.0, 0.9, 0.8], "wb_high": [1.2, 1.1, 1.0],
          "black": 0.05, "exposure": 1.1, "gamma": 6.9, "session": "selftest"}
    clean = {"wb_low": [1.0, 0.9, 0.8], "wb_high": [1.2, 1.1, 1.0],
             "black": 0.05, "exposure": 1.1, "gamma": 6.9}
    vios, _ = _ground_truth_violations("CLEAN", clean, gt)
    assert not vios, f"ground-truth self-test: clean params flagged {vios}"

    off = {"wb_low": [1.0, 0.5, 0.8], "wb_high": [1.2, 1.1, 1.0],
           "black": 0.5, "exposure": 1.5, "gamma": 6.0}
    vios, _ = _ground_truth_violations("OFF", off, gt)
    # wb_low[1], black, exposure, gamma all exceed tolerance
    assert len(vios) >= 4, f"ground-truth self-test: off params under-flagged {vios}"
    print("Ground-truth checker self-test: OK")


# AI-variant gate (spec 03): scene_tuner nudges the analytical params toward the
# user's per-scene aesthetic. The strict per-frame GT match (check_ground_truth)
# stays RED by design (irreducible taste); this gate instead requires the AI
# variant to be no WORSE than analytical on any param's aggregate median delta
# (a regression guard for the scene_tuner calibration) and to stay clip-safe.
AI_GT_REGRESS_EPS = 0.01   # per-param median delta the AI variant may grow by


def _load_scene_labels(roll):
    """Committed LLM scene labels for this roll (its own scene_labels.json) so
    the AI-variant gate runs offline without Ollama. Living inside the roll
    folder, it is inherently roll-matched. Returns {stem: label} or {}."""
    path = roll["dir"] / "scene_labels.json"
    if not path.is_file():
        return {}
    return json.loads(path.read_text()).get("labels", {})


def check_ai_variant(frames, fixtures, roll):
    """HARD gate for the spec-03 AI variant: build it with scene_tuner from the
    committed scene labels and compare its ground-truth deltas to analytical.
    FAILs only if the AI variant REGRESSES a param's aggregate median delta
    (beyond AI_GT_REGRESS_EPS) or clips beyond CLIP_MAX_FRAC — i.e. it guards
    that the scene tuning is a net improvement, while the strict per-frame match
    remains the (red-by-design) check_ground_truth gate. Returns violations."""
    import numpy as np
    labels = _load_scene_labels(roll)
    gt_by_stem = _load_ground_truth(fixtures)
    if not labels or not gt_by_stem:
        print("  (no committed scene labels / ground truth - AI gate skipped)")
        return []
    try:
        import scene_tuner as st
    except Exception as e:
        print(f"  (scene_tuner unavailable: {e})")
        return []
    by_stem = {fr["stem"]: fr for fr in frames}
    fields = [("wb_low", TOL_GT_WB), ("wb_high", TOL_GT_WB),
              ("D_max", TOL_GT_DMAX), ("offset", TOL_GT_OFFSET),
              ("black", TOL_GT_BLACK), ("exposure", TOL_GT_EXPOSURE),
              ("gamma", TOL_GT_GAMMA)]
    agg = {f: {"ana": [], "ai": []} for f, _ in fields}
    vio_ana = vio_ai = checked = 0
    worst_clip = 0.0
    for stem, gt in sorted(gt_by_stem.items()):
        fr = by_stem.get(stem)
        if fr is None or fr.get("error") or "params" not in fr or not fr.get("border"):
            continue
        scene = st._validate(labels[stem]) if labels.get(stem) else None
        try:
            enc_f, lin = an.load_frame(fr["path"], fr.get("vignette"))
        except Exception:
            continue
        ai, _ = st.apply_scene_tuning(fr["params"], scene, lin, fr["border"])
        checked += 1
        vio_ana += len(_ground_truth_violations(stem, fr["params"], gt)[0])
        vio_ai += len(_ground_truth_violations(stem, ai, gt)[0])
        l, t, r, b = fr["border"]
        h, w = lin.shape[:2]
        s = max(1, int(round(w * an.PRINT_TUNE_SUBSAMPLE_FRAC)))
        region = lin[t:h - b:s, l:w - r:s].reshape(-1, 3)
        if region.size:
            out = nm.render_negadoctor(region, ai)
            worst_clip = max(worst_clip,
                             float(np.mean((out >= CLIP_OUT_THR).any(axis=1))))
        for f, _tol in fields:
            tgt = gt.get(f)
            if tgt is None:
                continue
            if isinstance(tgt, list):
                agg[f]["ana"] += [abs(fr["params"][f][c] - tgt[c]) for c in range(3)]
                agg[f]["ai"] += [abs(ai[f][c] - tgt[c]) for c in range(3)]
            else:
                agg[f]["ana"].append(abs(fr["params"][f] - tgt))
                agg[f]["ai"].append(abs(ai[f] - tgt))

    def med(v):
        return sorted(v)[len(v) // 2] if v else float("nan")

    violations = []
    print(f"  checked {checked} frame(s); strict GT violations: analytical "
          f"{vio_ana} -> AI {vio_ai}")
    print(f"  AI variant worst hard-clip {worst_clip:.2%} "
          f"(limit {CLIP_MAX_FRAC:.0%})")
    print("  aggregate abs delta vs GT (analytical -> AI; AI must not regress):")
    for f, tol in fields:
        a, i = agg[f]["ana"], agg[f]["ai"]
        if not a:
            continue
        ma, mi = med(a), med(i)
        tag = ("better" if mi < ma - 1e-9
               else "worse" if mi > ma + 1e-9 else "same")
        print(f"    {f:9s} {ma:.4f} -> {mi:.4f}  ({tag}, tol {tol}, n={len(a)})")
        if mi > ma + AI_GT_REGRESS_EPS:
            violations.append(
                f"AI variant regressed {f}: median {ma:.4f} -> {mi:.4f}")
    if worst_clip > CLIP_MAX_FRAC:
        violations.append(
            f"AI variant hard-clip {worst_clip:.2%} > {CLIP_MAX_FRAC:.0%}")
    return violations


# Histogram-match gate (spec 04): the user's hard ground truth is the inverted
# PICTURE, not the (non-unique) params that made it, so the loss for the
# analytical print/wb tuning is the EMD between the algorithm's render and the GT
# render over the content crop. Like check_ground_truth this would be RED if used
# as a strict per-frame match (per-frame gamma/exposure is irreducible darkroom
# taste), so it is an informational dashboard + a REGRESSION guard against the
# committed histogram_baseline.json. TRUE clipping is gated separately by
# check_no_clipping (0.999); the near-white masses printed here are a coarser
# proxy and are informational only.
# The comparison runs on the FLOAT render (before 8-bit display quantization) at
# 14-bit bin resolution with a DENSER sample than the tuner, so the metric is
# faithful to fine structure incl. the highlight shoulder (8-bit/64-bin capped us
# at 256 levels and could not see highlights — user feedback). check_histogram_match
# also reports the top-highlight percentile deltas (hi999/hi9999).
HIST_BINS = nm.HIST_BINS_14          # 16384 (= "14-bit"), float comparison
HIST_SUBSAMPLE_FRAC = 0.001          # metric stride frac (denser than the tuner)
HIST_REGRESS_EPS = 0.01              # median total EMD may grow this much over baseline
HIST_HL_REGRESS_EPS = 0.01           # median |hi999| highlight delta may grow this much


def gt_params_for_frame(fr, gt):
    """Full negadoctor params that render the user's GT picture for this frame:
    the production params with ONLY the annotated fields overridden. Annotations
    are PARTIAL (some frames carry wb only), so a wb-only frame's GT picture is
    the production tone with the user's wb — never production replaced by
    defaults. Dmin/soft_clip stay = production (not annotated; offset and D_max
    ARE annotatable now). Pure copy; does not mutate fr['params']."""
    p = dict(fr["params"])
    if gt.get("wb_low"):
        p["wb_low"] = [float(v) for v in gt["wb_low"]]
    if gt.get("wb_high"):
        p["wb_high"] = [float(v) for v in gt["wb_high"]]
    for k in ("D_max", "offset", "black", "exposure", "gamma"):
        if gt.get(k) is not None:
            p[k] = float(gt[k])
    return p


def _render_crop_rows(lin, border, params, frac=None):
    """Render params over the subsampled content crop -> (N,3) FLOAT sRGB rows
    in [0,1] (NOT 8-bit — full precision so the histogram metric resolves fine
    highlight structure). `frac` is the stride fraction (default the metric's
    HIST_SUBSAMPLE_FRAC, denser than the tuner). None if empty.

    The render goes through `working_to_srgb` (darktable's colorout: linear
    Rec2020 working profile -> display sRGB), so the picture-match loss is
    measured in the SAME space the user evaluated and darktable EXPORTS — proper
    sRGB. Using the bare sRGB OETF on the Rec2020 values (the old bug) skewed the
    luma/chroma terms toward yellow, i.e. tuned the 'look' against colors neither
    the user nor darktable ever saw. sRGB (device-independent) is the right
    canonical space — NOT the monitor profile, which would tie tuning to one
    display."""
    l, t, r, b = border
    h, w = lin.shape[:2]
    s = max(1, int(round(w * (HIST_SUBSAMPLE_FRAC if frac is None else frac))))
    region = lin[t:h - b:s, l:w - r:s].reshape(-1, 3)
    if region.size == 0:
        return None
    return nm.working_to_srgb(nm.render_negadoctor(region, params))


HIST_TERMS = ("total", "luma", "color", "hi999", "hi9999",
              "hi_color", "clip_algo", "clip_gt")


def histogram_per_frame(frames, fixtures):
    """Per-frame picture-vs-picture histogram distance (production render vs the
    user's GT-param render, over each GT-annotated frame's content crop). The
    algorithm-INDEPENDENT 'look' metric: it compares two RENDERED images, never
    the params that made them. Returns a list of {stem, <HIST_TERMS>}; hi999/
    hi9999 are stored as |signed| magnitudes (top-end gap size). Shared single
    source of the metric — reused by check_histogram_match (median-reduced) and
    by the calibration runner (run_calibration.py)."""
    gt_by_stem = _load_ground_truth(fixtures)
    by_stem = {fr["stem"]: fr for fr in frames}
    records = []
    for stem, gt in sorted(gt_by_stem.items()):
        fr = by_stem.get(stem)
        if not fr or fr.get("error") or "params" not in fr or not fr.get("border"):
            continue
        try:
            enc_f, lin = an.load_frame(fr["path"], fr.get("vignette"))
        except Exception:
            continue
        gt_f = _render_crop_rows(lin, fr["border"], gt_params_for_frame(fr, gt))
        prod_f = _render_crop_rows(lin, fr["border"], fr["params"])
        if gt_f is None or prod_f is None:
            continue
        d = nm.histogram_distance(prod_f, gt_f, bins=HIST_BINS)
        records.append({
            "stem": stem,
            "total": d["total"], "luma": d["luma"], "color": d["color"],
            # highlight placement: |signed delta| (magnitude of the top-end gap)
            "hi999": abs(d["hi999"]), "hi9999": abs(d["hi9999"]),
            "hi_color": d["hi_color"],
            "clip_algo": d["top_a"], "clip_gt": d["top_b"],
        })
    return records


def _histogram_match_medians(frames, fixtures):
    """Median histogram-distance terms (production render vs GT render) over a
    roll's GT-annotated frames. Returns (medians, n) or (None, 0)."""
    records = histogram_per_frame(frames, fixtures)
    if not records:
        return None, 0

    def med(vals):
        return sorted(vals)[len(vals) // 2]

    return ({k: med([r[k] for r in records]) for k in HIST_TERMS},
            len(records))


def _histogram_violations(med, baseline):
    """Pure decision: FAIL if the median total EMD regressed past the committed
    baseline by more than HIST_REGRESS_EPS, or the median highlight-placement
    delta (|hi999|) regressed past baseline by HIST_HL_REGRESS_EPS. (TRUE
    clipping is gated by check_no_clipping; near-white mass here is
    informational.)"""
    v = []
    if med["total"] > baseline["total"] + HIST_REGRESS_EPS:
        v.append(f"histogram total EMD regressed {baseline['total']:.4f} -> "
                 f"{med['total']:.4f} (> +{HIST_REGRESS_EPS})")
    base_hl = baseline.get("hi999")
    if base_hl is not None and med["hi999"] > base_hl + HIST_HL_REGRESS_EPS:
        v.append(f"highlight |hi999| regressed {base_hl:.4f} -> "
                 f"{med['hi999']:.4f} (> +{HIST_HL_REGRESS_EPS})")
    return v


def _load_histogram_baseline(roll):
    path = roll["dir"] / "histogram_baseline.json"
    if not path.is_file():
        return None
    try:
        return json.loads(path.read_text())
    except Exception:
        return None


def check_histogram_match(frames, fixtures, roll):
    """Spec-04 gate: the algorithm's rendered output must stay close (in
    histogram EMD) to the user's GT render — the param-invariant ground truth.
    Informational dashboard + REGRESSION guard vs the committed
    histogram_baseline.json. Inert until that baseline exists (write it with
    calibrate_histogram_match.py --write-baseline after a review). Returns
    violations."""
    med, n = _histogram_match_medians(frames, fixtures)
    if not med:
        print("  (no wb/print ground-truth annotations)")
        return []
    print(f"  checked {n} frame(s) (float 14-bit, stride {HIST_SUBSAMPLE_FRAC}); "
          f"median EMD to GT: total {med['total']:.4f}  luma {med['luma']:.4f}  "
          f"color {med['color']:.4f}")
    print(f"  top-highlight median |Δ| P99.9 {med['hi999']:.4f}  P99.99 "
          f"{med['hi9999']:.4f}  hl-color {med['hi_color']:.4f}")
    print(f"  near-white mass (informational): algo {med['clip_algo']:.2%} "
          f"vs gt {med['clip_gt']:.2%}")
    baseline = _load_histogram_baseline(roll)
    if not baseline:
        print(f"  no histogram_baseline.json yet for {roll['id']} — write it via")
        print("  calibrate_histogram_match.py --write-baseline after a review")
        return []
    if (baseline.get("bins") != HIST_BINS or abs(
            baseline.get("subsample_frac", -1) - HIST_SUBSAMPLE_FRAC) > 1e-9):
        print("  baseline metric params differ (bins/subsample) — regenerate; "
              "skipping regression check")
        return []
    vios = _histogram_violations(med, baseline)
    print(f"  baseline total {baseline['total']:.4f} -> now {med['total']:.4f} "
          f"({'REGRESSED' if vios else 'ok'})")
    return vios


def selftest_histogram_match():
    """Test for the test: clean medians pass; a total-EMD OR a highlight (hi999)
    regression is flagged."""
    base = {"total": 0.060, "hi999": 0.030}
    clean = {"total": 0.060, "luma": 0.05, "color": 0.02, "hi999": 0.030,
             "clip_algo": 0.01, "clip_gt": 0.005}
    assert not _histogram_violations(clean, base), \
        "histogram self-test: clean medians flagged"
    worse = dict(clean, total=0.060 + HIST_REGRESS_EPS + 0.005)
    assert _histogram_violations(worse, base), \
        "histogram self-test: total regression not flagged"
    hl_worse = dict(clean, hi999=0.030 + HIST_HL_REGRESS_EPS + 0.005)
    assert _histogram_violations(hl_worse, base), \
        "histogram self-test: highlight regression not flagged"
    print("Histogram-match checker self-test: OK")


def check_no_clipping(frames):
    """HARD gate the param ground-truth gate does NOT cover: render each frame's
    PRODUCTION params and flag any whose hard-clip fraction (pixels with any
    channel >= CLIP_OUT_THR) exceeds CLIP_MAX_FRAC. Subsamples for speed, using
    the same stride the print tuner uses. Returns a list of violation strings."""
    import numpy as np
    violations, worst = [], []
    for fr in frames:
        if fr.get("error") or "params" not in fr or not fr.get("border"):
            continue
        try:
            enc_f, lin = an.load_frame(fr["path"], fr.get("vignette"))
        except Exception:
            continue
        l, t, r, b = fr["border"]
        h, w = lin.shape[:2]
        s = max(1, int(round(w * an.PRINT_TUNE_SUBSAMPLE_FRAC)))
        region = lin[t:h - b:s, l:w - r:s].reshape(-1, 3)
        if region.size == 0:
            continue
        out = nm.render_negadoctor(region, fr["params"])
        clip = float(np.mean((out >= CLIP_OUT_THR).any(axis=1)))
        worst.append((fr["stem"], clip))
        if clip > CLIP_MAX_FRAC:
            violations.append(f"{fr['stem']}: hard-clip fraction {clip:.1%} "
                              f"> {CLIP_MAX_FRAC:.0%} (overexposed / unusable)")
    worst.sort(key=lambda x: -x[1])
    if worst:
        print("  checked {} frame(s); worst clip: {}".format(
            len(worst), ", ".join(f"{s} {c:.2%}" for s, c in worst[:3])))
    return violations


def run_roll(roll_info):
    """Run the full check suite for ONE roll. Returns (fail, warn)."""
    images, exif, fixtures = (roll_info["images"], roll_info["exif"],
                              roll_info["fixtures"])

    print(f"Running pipeline on {len(images)} test frame(s) "
          f"({Path(images[0]).suffix})...")
    frames, roll = an.process_roll(images, exif)
    errors = [f for f in frames if f.get("error")]
    for fr in errors:
        print(f"  ERR {fr['stem']}: {fr['error']}")

    if roll and roll.get("vignette"):
        v, vi = roll["vignette"], roll.get("vignette_info") or {}
        print(f"Vignette: strength={v['strength']:.3f} radius={v['radius']:.3f} "
              f"steepness={v['steepness']:.3f} "
              f"(corner falloff {vi.get('corner_falloff', 0):.1%}, "
              f"residual {vi.get('residual')})")

    print()
    print("--- Invariants ---")
    violations = check_frame_invariants(frames, roll)
    if violations:
        for v in violations:
            print(f"  VIOLATION: {v}")
    else:
        print("  all invariants hold")

    print()
    print("--- Crop containment (HARD rule: never outside user crops) ---")
    containment = check_crop_containment(frames, fixtures)
    for v in containment:
        print(f"  VIOLATION: {v}")

    print()
    print("--- Annotated frames (user corrections; informational) ---")
    report_annotated_frames(frames, fixtures)

    print()
    print("--- Ground truth (HARD gate: algorithm must match user's "
          "wheel/print annotations) ---")
    ground_truth = check_ground_truth(frames, fixtures)
    for v in ground_truth:
        print(f"  VIOLATION: {v}")

    print()
    print("--- AI variant (HARD gate: scene_tuner must improve, not regress, "
          "vs analytical) ---")
    ai_variant = check_ai_variant(frames, fixtures, roll_info)
    for v in ai_variant:
        print(f"  VIOLATION: {v}")
    if not ai_variant:
        print("  AI variant improves/holds every param and is clip-safe")

    print()
    print("--- No clipping (HARD gate: production render must not blow out "
          "highlights) ---")
    clipping = check_no_clipping(frames)
    for v in clipping:
        print(f"  VIOLATION: {v}")
    if not clipping:
        print("  no frame exceeds the hard-clip limit")

    print()
    print("--- Histogram match (spec 04: render must match the GT render; "
          "regression guard vs baseline) ---")
    hist_match = check_histogram_match(frames, fixtures, roll_info)
    for v in hist_match:
        print(f"  VIOLATION: {v}")

    print()
    print("--- Baseline diff ---")
    baseline = load_baseline(roll_info["id"])
    diff_frames = 0
    compared = 0
    winner_changed = False
    if not baseline:
        print(f"  no baseline_session/{roll_info['id']}/ yet - run "
              "generate_baseline.py after a")
        print("  user-approved debug-UI review to enable regression diffs")
    else:
        base_winner = next((b["film_base"]["global_winner_stem"]
                            for b in baseline.values()
                            if b.get("film_base")), None)
        if roll and base_winner and roll["winner_stem"] != base_winner:
            winner_changed = True
            print(f"  GLOBAL WINNER CHANGED: {roll['winner_stem']} vs {base_winner}")
        for fr in frames:
            if fr.get("error") or fr["stem"] not in baseline:
                continue
            compared += 1
            diffs = diff_frame_vs_baseline(fr, baseline[fr["stem"]])
            if diffs:
                diff_frames += 1
                print(f"  {fr['stem']}:")
                for d in diffs:
                    print(f"    {d}")
        print(f"  {diff_frames}/{compared} frame(s) differ from baseline")

    print()
    fail = (bool(violations) or bool(errors) or bool(containment)
            or bool(ground_truth) or bool(clipping) or bool(ai_variant)
            or bool(hist_match))
    warn = False
    if compared:
        frac = diff_frames / compared
        if winner_changed or frac > WARN_DIFF_FRAC:
            fail = True
        elif frac > PASS_DIFF_FRAC:
            warn = True

    verdict = "FAIL" if fail else ("WARN" if warn else "PASS")
    print(f"ROLL {roll_info['id']} VERDICT: {verdict}")
    return fail, warn


