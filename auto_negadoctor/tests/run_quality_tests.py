import math
import os
import sys
from pathlib import Path

TESTS_DIR = Path(os.path.dirname(os.path.abspath(__file__)))
sys.path.insert(0, str(TESTS_DIR.parent))
import auto_negadoctor as an
import nega_model as nm

IMAGES_DIR = TESTS_DIR / "images"            # sRGB JPEG fallback fixtures
IMAGES_TIF_DIR = TESTS_DIR / "images_tif"    # real linear-Rec2020 TIFF exports
ANNOTATIONS_DIR = TESTS_DIR / "fixtures" / "annotations"
BASELINE_DIR = TESTS_DIR / "baseline_session"


def list_test_images():
    """Prefer the TIFF fixtures (real export format) over the JPEGs.
    Returns (paths, exif_by_stem)."""
    tifs = sorted(IMAGES_TIF_DIR.glob("*.tif"))
    if tifs:
        import auto_negadoctor as _an
        exif = _an.parse_exif_params(str(IMAGES_TIF_DIR / "exif_params.txt"))
        return [str(p) for p in tifs], exif
    return [str(p) for p in sorted(IMAGES_DIR.glob("*.jpg"))], {}

# Baseline diff tolerances (deterministic pipeline; slack covers const tweaks)
TOL_DMIN_REL = 0.10
TOL_DMAX = 0.20
TOL_WB = 0.10
TOL_BLACK = 0.05
TOL_EXPOSURE = 0.05
TOL_PATCH_CENTER_PX = 30.0
TOL_PATCH_SIZE_PX = 10
# Verdict gates: fraction of frames allowed to differ from baseline
PASS_DIFF_FRAC = 0.10
WARN_DIFF_FRAC = 0.30

WB_NORM_EPS = 1e-3
EXPOSURE_ORDER_REL_TOL = 0.02   # Dmin_i/Dmin_j vs factor_i/factor_j
CROP_CONTAINMENT_ROUND_TOL = 1  # px slack for cross-resolution crop denorm

# Ground-truth gate tolerances (the user's wheel/print annotations are the
# target the production algorithm must converge to). Deliberately strict; the
# gate is expected to FAIL until the wb finder + print tune (+ PRINT_GAMMA) are
# tuned to match. Loosen ONLY with the user's sign-off.
TOL_GT_WB = 0.05         # abs delta per wb_low/wb_high component
TOL_GT_BLACK = 0.03
TOL_GT_EXPOSURE = 0.05
TOL_GT_GAMMA = 0.10

# Hard-clip gate: the param gate does NOT measure clipping, yet a tuner that
# matches the param values can still blow out the highlights (a shadow-anchor
# attempt clipped 3-60% of pixels on normal scenes — the user reported it
# unusable on re-export). Rendering the user's own GT params clips ~0% on every
# frame, so the production print tune must too.
CLIP_OUT_THR = 0.999     # rendered output at/above this = a hard-clipped pixel
CLIP_MAX_FRAC = 0.01     # max fraction of hard-clipped pixels allowed per frame

# Annotation crop rects are stored as NORMALIZED fractions of the frame
# (resolution-independent). Legacy pixel rects (any coord > 1.5) pass through.
def _rect_to_px(rect, w, h):
    """[x, y, rw, rh] fractions -> integer pixels (legacy px rects unchanged)."""
    if max(abs(float(v)) for v in rect) > 1.5:
        return [int(round(float(v))) for v in rect]
    x, y, rw, rh = rect
    return [int(round(x * w)), int(round(y * h)),
            int(round(rw * w)), int(round(rh * h))]


# Annotation fixtures span MULTIPLE rolls (stems collide: every roll has a
# DSC_0013 etc.), but the by-stem checks compare against whatever roll is
# currently loaded in images_tif. A fixture session from a DIFFERENT roll would
# be matched against the wrong image. Each roll's images_tif and each session
# folder carry a `roll.txt` naming the roll; a session is used only when its
# roll matches the loaded roll. Legacy data with no roll.txt is always included
# (a session/loaded roll is excluded only when BOTH ids are known and differ).
def _read_roll_id(path):
    return path.read_text().strip() if path.is_file() else None


def _roll_fixtures():
    """Committed annotation fixtures whose roll matches the loaded images_tif
    roll (see _read_roll_id). Replaces a bare ANNOTATIONS_DIR.rglob so a
    second roll's annotations can coexist without cross-roll stem collisions."""
    if not ANNOTATIONS_DIR.is_dir():
        return []
    loaded = _read_roll_id(IMAGES_TIF_DIR / "roll.txt")
    out = []
    for f in sorted(ANNOTATIONS_DIR.rglob("*_annotations.json")):
        sess = _read_roll_id(f.parent / "roll.txt")
        if loaded is not None and sess is not None and sess != loaded:
            continue
        out.append(f)
    return out


