"""Quality / regression suite for auto_negadoctor.

Two layers:
  1. Baseline-independent INVARIANTS over a fresh pipeline run on
     tests/images (param ranges, wb normalization, exposure-ordering
     consistency of Dmin across frames, patches inside frame bounds and
     outside the holder border, hex round-trip).
  2. Baseline diff vs tests/baseline_session/{stem}_debug_nega.json
     (generated by generate_baseline.py after user approval): global winner
     identity, film-base location, params within tolerances.

Run: conda run -n autocrop python auto_negadoctor/tests/run_quality_tests.py
Exit code: 0 = PASS or WARN, 1 = FAIL.
"""

import json
import math
import os
import sys
from pathlib import Path

TESTS_DIR = Path(os.path.dirname(os.path.abspath(__file__)))
sys.path.insert(0, str(TESTS_DIR.parent))
import auto_negadoctor as an
import nega_model as nm

IMAGES_DIR = TESTS_DIR / "images"            # sRGB JPEG fallback fixtures
IMAGES_TIF_DIR = TESTS_DIR / "images_tif"    # real linear-Rec2020 TIFF exports
ANNOTATIONS_DIR = TESTS_DIR / "fixtures" / "annotations"
BASELINE_DIR = TESTS_DIR / "baseline_session"


def list_test_images():
    """Prefer the TIFF fixtures (real export format) over the JPEGs.
    Returns (paths, exif_by_stem)."""
    tifs = sorted(IMAGES_TIF_DIR.glob("*.tif"))
    if tifs:
        import auto_negadoctor as _an
        exif = _an.parse_exif_params(str(IMAGES_TIF_DIR / "exif_params.txt"))
        return [str(p) for p in tifs], exif
    return [str(p) for p in sorted(IMAGES_DIR.glob("*.jpg"))], {}

# Baseline diff tolerances (deterministic pipeline; slack covers const tweaks)
TOL_DMIN_REL = 0.10
TOL_DMAX = 0.20
TOL_WB = 0.10
TOL_BLACK = 0.05
TOL_EXPOSURE = 0.05
TOL_PATCH_CENTER_PX = 30.0
TOL_PATCH_SIZE_PX = 10
# Verdict gates: fraction of frames allowed to differ from baseline
PASS_DIFF_FRAC = 0.10
WARN_DIFF_FRAC = 0.30

WB_NORM_EPS = 1e-3
EXPOSURE_ORDER_REL_TOL = 0.02   # Dmin_i/Dmin_j vs factor_i/factor_j


# ---------------------------------------------------------------------------
# Invariants (baseline-independent)
# ---------------------------------------------------------------------------

def check_frame_invariants(frames, roll):
    """Return a list of violation strings (empty = all good)."""
    bad = []

    def chk(cond, stem, msg):
        if not cond:
            bad.append(f"{stem}: {msg}")

    ok_frames = [f for f in frames if not f.get("error") and f.get("params")]
    for fr in ok_frames:
        stem = fr["stem"]
        p = fr["params"]

        for name, val, rng in (
            ("D_max", p["D_max"], nm.DMAX_RANGE),
            ("offset", p["offset"], nm.OFFSET_RANGE),
            ("black", p["black"], nm.BLACK_RANGE),
            ("exposure", p["exposure"], nm.EXPOSURE_RANGE),
        ):
            chk(math.isfinite(val) and rng[0] <= val <= rng[1], stem,
                f"{name}={val} outside {rng}")
        for c in range(3):
            chk(nm.DMIN_RANGE[0] <= p["Dmin"][c] <= nm.DMIN_RANGE[1], stem,
                f"Dmin[{c}]={p['Dmin'][c]} outside range")
            chk(nm.WB_RANGE[0] <= p["wb_low"][c] <= nm.WB_RANGE[1], stem,
                f"wb_low[{c}] outside range")
            chk(nm.WB_RANGE[0] <= p["wb_high"][c] <= nm.WB_RANGE[1], stem,
                f"wb_high[{c}] outside range")
        chk(all(math.isfinite(v) for v in p["Dmin"] + p["wb_low"] + p["wb_high"]),
            stem, "non-finite param component")

        # picker normalization: wb_low max == 1, wb_high min == 1
        chk(abs(max(p["wb_low"]) - 1.0) <= WB_NORM_EPS, stem,
            f"max(wb_low)={max(p['wb_low'])} != 1")
        chk(abs(min(p["wb_high"]) - 1.0) <= WB_NORM_EPS, stem,
            f"min(wb_high)={min(p['wb_high'])} != 1")

        # film base must stay orange
        chk(p["Dmin"][0] > p["Dmin"][1] > p["Dmin"][2], stem,
            f"Dmin not orange-ordered: {p['Dmin']}")

        # print curve: pipeline gamma (user taste), darktable-default soft_clip
        chk(p["gamma"] == an.PRINT_GAMMA and p["soft_clip"] == nm.SOFT_CLIP_DEFAULT,
            stem, "gamma/soft_clip not at pipeline defaults")

        # hex blob round-trip
        d = nm.decode_negadoctor_params(fr["params_hex"])
        chk(abs(d["exposure"] - p["exposure"]) < 1e-6
            and all(abs(a - b) < 1e-6 for a, b in zip(d["Dmin"], p["Dmin"])),
            stem, "params_hex does not round-trip to params")

        # patch rects inside frame bounds; shadows/highlights additionally
        # inside the content crop (the film-base rect may legitimately sit in
        # the rejected ring — base lives in gaps/rebate, and the crop governs
        # LEVEL computations, not the base search)
        w, h = fr["width"], fr["height"]
        l, t, r, b = fr["border"]
        for kind in ("base", "shadow_patch", "highlight_patch"):
            obj = fr.get(kind) if kind != "base" else fr.get("base")
            if not obj:
                continue
            x, y, ww, hh = obj["rect"]
            chk(0 <= x and 0 <= y and x + ww <= w and y + hh <= h, stem,
                f"{kind} rect {obj['rect']} out of bounds")
            if kind != "base":
                chk(x >= l and y >= t and x + ww <= w - r and y + hh <= h - b,
                    stem,
                    f"{kind} rect {obj['rect']} outside content crop {fr['border']}")

    # vignette: roll-level params sane and fit trustworthy
    vig_frames = [f for f in ok_frames if f.get("vignette")]
    if vig_frames:
        v = vig_frames[0]["vignette"]
        for name in ("strength", "radius", "steepness"):
            val = v.get(name)
            chk(val is not None and 0.0 <= val <= 1.0, "roll",
                f"vignette {name}={val} outside [0,1]")

    # exposure-ordering consistency: Dmin scales with the exposure factor
    cmp = [f for f in ok_frames if not f["exif"].get("missing_exif")]
    for i in range(len(cmp)):
        for j in range(i + 1, len(cmp)):
            fi, fj = cmp[i], cmp[j]
            fact_ratio = fi["exposure_factor"] / fj["exposure_factor"]
            for c in range(3):
                a, b_ = fi["params"]["Dmin"][c], fj["params"]["Dmin"][c]
                # skip channels saturated by the clamp
                if (a >= nm.DMIN_RANGE[1] - 1e-6 or b_ >= nm.DMIN_RANGE[1] - 1e-6):
                    continue
                ratio = a / b_
                if abs(ratio / fact_ratio - 1.0) > EXPOSURE_ORDER_REL_TOL:
                    bad.append(f"{fi['stem']}/{fj['stem']}: Dmin[{c}] ratio "
                               f"{ratio:.4f} vs exposure ratio {fact_ratio:.4f}")
    return bad


def selftest_invariants():
    """Test for the test: a deliberately broken frame must be flagged."""
    base = {
        "stem": "FAKE", "error": None, "width": 1000, "height": 660,
        "border": (10, 10, 10, 10),
        "exif": {"missing_exif": False}, "exposure_factor": 1.0,
        "base": {"rect": [100, 100, 40, 40]},
        "shadow_patch": None, "highlight_patch": None,
    }
    params = {
        "Dmin": [0.7, 0.3, 0.15], "wb_high": [1.2, 1.1, 1.0],
        "wb_low": [1.0, 0.9, 0.8], "D_max": 2.0, "offset": -0.05,
        "black": 0.1, "gamma": an.PRINT_GAMMA,
        "soft_clip": nm.SOFT_CLIP_DEFAULT, "exposure": 0.95, "film_stock": 1,
    }
    good = dict(base, params=params,
                params_hex=nm.encode_negadoctor_params(params))
    assert not check_frame_invariants([good], None), \
        "invariant self-test: clean frame was flagged"

    broken_params = dict(params, wb_low=[0.9, 0.8, 0.7],          # max != 1
                         Dmin=[0.15, 0.3, 0.7])                    # not orange
    broken = dict(base, params=broken_params,
                  params_hex=nm.encode_negadoctor_params(broken_params),
                  shadow_patch={"rect": [2, 2, 40, 40]})           # outside crop
    violations = check_frame_invariants([broken], None)
    assert len(violations) >= 3, \
        f"invariant self-test: broken frame produced only {violations}"
    print("Invariant checker self-test: OK")


